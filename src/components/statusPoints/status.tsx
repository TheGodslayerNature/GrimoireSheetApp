export class Status {
    forca:number;
    tec:number;
    vit:number;
    mag:number;
    agi:number;
    sor:number;

    constructor(forca:number, tec:number, vit:number, mag:number, agi:number, sor:number ) {
        this.forca = forca;
        this.tec = tec;
        this.vit = vit;
        this.mag = mag;
        this.agi = agi;
        this.sor = sor;
    }
}